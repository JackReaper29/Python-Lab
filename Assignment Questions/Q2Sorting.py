def insertion_sort(arr):
    for i in range(1, len(arr)):
        key = arr[i]
        j = i - 1
        while j >= 0 and key < arr[j]:
            arr[j + 1] = arr[j]
            j -= 1
        arr[j + 1] = key

def selection_sort(arr):
    for i in range(len(arr)):
        min_index = i
        for j in range(i + 1, len(arr)):
            if arr[j] < arr[min_index]:
                min_index = j
        arr[i], arr[min_index] = arr[min_index], arr[i]

def bubble_sort(arr):
    n = len(arr)
    for i in range(n):
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]

def merge_sort(arr):
    if len(arr) > 1:
        mid = len(arr) // 2
        left_half = arr[:mid]
        right_half = arr[mid:]

        merge_sort(left_half)
        merge_sort(right_half)

        i = j = k = 0

        while i < len(left_half) and j < len(right_half):
            if left_half[i] < right_half[j]:
                arr[k] = left_half[i]
                i += 1
            else:
                arr[k] = right_half[j]
                j += 1
            k += 1

        while i < len(left_half):
            arr[k] = left_half[i]
            i += 1
            k += 1

        while j < len(right_half):
            arr[k] = right_half[j]
            j += 1
            k += 1

# Example usage:
unsorted_list = [12, 11, 13, 5, 6]
print("Original List:", unsorted_list.copy())

insertion_sorted = unsorted_list.copy()
insertion_sort(insertion_sorted)
print("Insertion Sort:", insertion_sorted)

selection_sorted = unsorted_list.copy()
selection_sort(selection_sorted)
print("Selection Sort:", selection_sorted)

bubble_sorted = unsorted_list.copy()
bubble_sort(bubble_sorted)
print("Bubble Sort:", bubble_sorted)

merge_sorted = unsorted_list.copy()
merge_sort(merge_sorted)
print("Merge Sort:", merge_sorted)
