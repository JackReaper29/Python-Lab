# Creating a dictionary
my_dict = {'a': 1, 'b': 2, 'c': 3}

# Accessing values by key
print(my_dict['b'])  # Output: 2

# Modifying values
my_dict['c'] = 4
print(my_dict)  # Output: {'a': 1, 'b': 2, 'c': 4}

# Adding new key-value pairs
my_dict['d'] = 5
print(my_dict)  # Output: {'a': 1, 'b': 2, 'c': 4, 'd': 5}
