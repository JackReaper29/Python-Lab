# Creating a list
my_list = [1, 2, 3, 4, 5]

# Accessing elements
print(my_list[0])  # Output: 1

# Modifying elements
my_list[1] = 10
print(my_list)  # Output: [1, 10, 3, 4, 5]

# Adding elements
my_list.append(6)
print(my_list)  # Output: [1, 10, 3, 4, 5, 6]

# Removing elements
my_list.remove(3)
print(my_list)  # Output: [1, 10, 4, 5, 6]
